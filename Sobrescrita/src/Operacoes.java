public class Operacoes {
    int soma(int num1, int num2, int num3){
        return num1 + num2 + num3;
    }

    int soma(int num1, int num2){
        return num1 + num2;
    }

    // Sobrecarga (ou polimorfismo estático) acontece quando temos métodos com o mesmo nome numa classe, porém com parâmetros diferentes e não faz uso da anotation que seria o @ com Over...

}
